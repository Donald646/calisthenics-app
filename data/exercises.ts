// Helper: get exercises by pattern and level
export function getExercisesByPattern(pattern: string, level?: number): Exercise[] {
  return exercises.filter(
    (e) => e.pattern === pattern && (level === undefined || e.level === level)
  );
}

// Helper: get exercise by ID
export function getExerciseById(id: string): Exercise | undefined {
  return exercises.find((e) => e.id === id);
}
